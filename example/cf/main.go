func main() {
	g := cf.NewGame()

	g.Add(0, 1)
	fmt.Printf("%s\n", g)
	fmt.Printf("%v\n", g.Win(0))

	g.Add(0, 2)
	fmt.Printf("%s\n", g)
	fmt.Printf("%v\n", g.Win(0))

	g.Add(0, 3)
	fmt.Printf("%s\n", g)
	fmt.Printf("%v\n", g.Win(0))

	g.Add(0, 0)
	fmt.Printf("%s\n", g)
	fmt.Printf("%v\n", g.Win(0))

}

